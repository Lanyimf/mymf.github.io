def location_conditions(items_list):
    return {"category_name": "第二類 區位條件（Location Condition）", "items": items_list}


def spatial_conditions(items_list):
    return {"category_name": "第三類 空間條件（Spatial Condition）", "items": items_list}


def specific_conditions(use_item, items_list):
    return {"category_name": f"第四類 {use_item}特定條件", "items": items_list}


