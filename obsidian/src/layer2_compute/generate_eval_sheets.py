# ── Excel 工具函式 ────────────────────────────────────────────────
def write_sheet(wb, eval_code, land_code, land_name, use_item, data):
    title_text = f"{eval_code} {land_name}－{use_item}評估指標表"
    ws_name = f"{eval_code} {land_name}-{use_item}"[:31]

    if ws_name in wb.sheetnames:
        del wb[ws_name]
    ws = wb.create_sheet(ws_name)

    # 標題
    ws.merge_cells("A1:D1")
    ws["A1"] = title_text
    ws["A1"].font = TITLE_FONT
    ws["A1"].alignment = CENTER
    ws.row_dimensions[1].height = 28

    # 欄頭
    for col, h in enumerate(["#", "指標項目", "指標屬性", "評估標準 / 量化指標"], 1):
        c = ws.cell(row=2, column=col, value=h)
        c.font = HDR_FONT
        c.fill = HDR_FILL
        c.alignment = CENTER
        c.border = BORDER
    ws.row_dimensions[2].height = 18

    row = 3
    for cat in data.get("categories", []):
        ws.merge_cells(f"A{row}:D{row}")
        c = ws.cell(row=row, column=1, value=cat["category_name"])
        c.font = CAT_FONT
        c.fill = CAT_FILL
        c.alignment = WRAP
        ws.row_dimensions[row].height = 16
        row += 1

        for item in cat.get("items", []):
            no, name, itype, std = item[0], item[1], item[2], item[3]
            fill = TYPE_FILL.get(itype, R_FILL)
            for col, val in enumerate([no, name, itype, std], 1):
                c = ws.cell(row=row, column=col, value=val)
                c.font = BODY_FONT
                c.fill = fill
                c.alignment = WRAP
                c.border = BORDER
            # 動態列高
            lines = max(1, len(str(std)) // 60 + std.count("\n") + 1)
            ws.row_dimensions[row].height = max(15, lines * 14)
            row += 1

    # 圖例
    row += 1
    ws.cell(row=row, column=1, value="【屬性說明】").font = Font(bold=True, name="Arial", size=9)
    ws.cell(row=row+1, column=1,
            value="硬性門檻（黃底）：不符合即淘汰，為前置篩選條件。\n"
                  "重要指標（綠底）：嚴重影響可行性，評估不可缺。\n"
                  "條件性指標（藍底）：依場址位置或規模而定，部分需申請許可。\n"
                  "參考指標（白底）：影響效益與效率，建議納入可行性分析。"
           ).font = BODY_FONT

    # 欄寬
    ws.column_dimensions["A"].width = 5
    ws.column_dimensions["B"].width = 20
    ws.column_dimensions["C"].width = 13
    ws.column_dimensions["D"].width = 72

    print(f"  ✓ {eval_code} {use_item}")


def main():
    print(f"載入來源 Excel...")
    try:
        wb = openpyxl.load_workbook(SRC_FILE)
        print(f"  已載入：{SRC_FILE}")
    except Exception as e:
        print(f"  無法載入來源（{e}），建立新 Excel")
        wb = openpyxl.Workbook()
        wb.remove(wb.active)

    total = len(SHEETS)
    print(f"\n生成 {total} 張評估指標表...")
    for eval_code, info in SHEETS.items():
        write_sheet(wb, eval_code,
                    info["land_code"], info["land_name"],
                    info["use_item"], info)

    wb.save(OUT_FILE)
    print(f"\n✅ 完成！儲存至：{OUT_FILE}")
    print(f"   共生成 {total} 張評估指標工作表")


if __name__ == "__main__":
    main()
